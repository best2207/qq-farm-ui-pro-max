const fs = require('node:fs')
const path = require('node:path')

const repoRoot = path.resolve(__dirname, '../..')
const changelogPath = path.join(repoRoot, 'CHANGELOG.md')
const outputPath = path.join(repoRoot, 'web/src/generated/help-release-notes.ts')

function parseQuickIndex(markdown) {
  const quickIndexMatch = markdown.match(/## 快速索引（精简版）([\s\S]*?)(?:\n> 说明|\n## )/)
  const quickIndexBlock = quickIndexMatch?.[1] || ''

  return quickIndexBlock
    .split('\n')
    .map(line => line.trim())
    .filter(Boolean)
    .map((line) => {
      const match = line.match(/^- `([^`]+) \((\d{4}-\d{2}-\d{2})\)` (.+)$/)
      if (!match)
        return null

      const [, version = '', date = '', content = ''] = match
      const [titlePart, summaryPart] = content.split(/[：:]/, 2)

      return {
        version,
        date,
        title: titlePart?.trim() || content.trim(),
        summary: summaryPart?.trim() || content.trim(),
      }
    })
    .filter(Boolean)
}

function createModuleSource(releaseNotes) {
  const quote = value => `'${String(value).replace(/\\/g, '\\\\').replace(/'/g, '\\\'')}'`
  const serialized = releaseNotes
    .map((releaseNote) => {
      return [
        '  {',
        `    version: ${quote(releaseNote.version)},`,
        `    date: ${quote(releaseNote.date)},`,
        `    title: ${quote(releaseNote.title)},`,
        `    summary: ${quote(releaseNote.summary)},`,
        '  },',
      ].join('\n')
    })
    .join('\n')
  return `// Auto-generated by scripts/utils/sync-help-release-notes.js
// Do not edit manually. Update CHANGELOG.md and re-run the sync script instead.

export const helpReleaseNotes = [
${serialized}
] as const
`
}

function main() {
  const changelog = fs.readFileSync(changelogPath, 'utf8')
  const releaseNotes = parseQuickIndex(changelog)
  fs.mkdirSync(path.dirname(outputPath), { recursive: true })
  fs.writeFileSync(outputPath, createModuleSource(releaseNotes), 'utf8')
  console.log(`[help-center] synced ${releaseNotes.length} release notes to ${path.relative(repoRoot, outputPath)}`)
}

main()
